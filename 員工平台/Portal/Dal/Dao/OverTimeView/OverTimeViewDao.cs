                if (Vdb.Payload != null)
                {
                    //實作DTO轉換

                }
            }
            return Vdb;
        }

        public APIResult GetData(OverTimeViewConditions Cond, CancellationToken cancellationToken = default(CancellationToken))
        {
            var Vdb = Post(Cond, cancellationToken);

            if (Vdb.Status)
            {
                if (Vdb.Data != null)
                {
                    if (Vdb.Payload != null && Vdb.Data != null)
                    {
                        var oSource = Vdb.Data as OverTimeViewApiRow;

                        if (oSource != null)
                        {
                            Vdb.Status = oSource.state;
                            Vdb.Message = oSource.message;
                            Vdb.StackTrace = oSource.stackTrace;

                            if (oSource.state)
                            {
                                if (oSource.result != null)
                                {
                                    var rsSource = oSource.result;
                                    var rsTarget = new List<OverTimeViewRow>();

                                    //把api的Data轉成我們的Data
                                    foreach (var rSource in rsSource)
                                    {
                                        var rTarget = new OverTimeViewRow();
                                        rTarget.EmpId = rSource.employeeID;
                                        rTarget.EmpName = rSource.employeeName;
                                        rTarget.DateA = rSource.overTimeDate;
                                        rTarget.OtBeginTime = rSource.beginTime;
                                        rTarget.OtEndTime = rSource.endTime;
                                        rTarget.TotalTime = rSource.overTimeTotalHours;
                                        rTarget.OtTime = rSource.overTimeHours;
                                        rTarget.RestTime = rSource.restTimeHours;
                                        rTarget.OtReason = rSource.overTimeReason;
                                        rTarget.Note = rSource.remarks;
                                        rTarget.Yymm = rSource.yymm;

                                        rsTarget.Add(rTarget);
                                    }

                                    Vdb.Data = rsTarget;
                                }
                            }
                        }
                    }
                }
            }

            return Vdb;
        }
    }
}