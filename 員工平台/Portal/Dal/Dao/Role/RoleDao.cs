            if (Vdb.Status)
            {
                if (Vdb.Payload != null)
                {
                    //實作DTO轉換

                }
            }
            return Vdb;
        }

        public APIResult GetData(RoleConditions Cond, CancellationToken cancellationToken = default(CancellationToken))
        {
            var Vdb = Post(Cond, cancellationToken);

            if (Vdb.Status)
            {
                if (Vdb.Data != null)
                {
                    if (Vdb.Payload != null && Vdb.Data != null)
                    {
                        //實作DTO轉換
                        var oSource = Vdb.Data as RoleApiRow;
                        if (oSource != null)
                        {
                            Vdb.Status = oSource.state;
                            Vdb.Message = oSource.message;
                            Vdb.StackTrace = oSource.stackTrace;
                            var rsSource = oSource;
                            var rsTarget = new List<RoleRow>();

                            foreach (var rSource in rsSource.result)
                            {
                                
                                var rTarget = new RoleRow();
                                rTarget.Code = rSource.code;
                                rTarget.Name = rSource.name;
                                rTarget.IsAdminRole = rSource.isAdminRole;
                                rTarget.IsVisible = rSource.isVisible;

                                rsTarget.Add(rTarget);
                                
                            }
                            Vdb.Data = rsTarget;
                        }
                    }
                }
            }

            return Vdb;
        }
    }
}