                }
            }
            return Vdb;
        }

        public APIResult GetData(TemperoturyReportConditions Cond, CancellationToken cancellationToken = default(CancellationToken))
        {
            var Vdb = Post(Cond, cancellationToken);

            if (Vdb.Status)
            {
                if (Vdb.Data != null)
                {
                    if (Vdb.Payload != null && Vdb.Data != null)
                    {
                        //實作DTO轉換
                        var oSource = Vdb.Data as TemperoturyReportApiRow;
                        if (oSource != null)
                        {
                            Vdb.Status = oSource.state;
                            Vdb.Message = oSource.message;
                            Vdb.StackTrace = oSource.stackTrace;
                            var rsSource = oSource;
                            var rsTarget = new List<TemperoturyReportRow>();

                            foreach (var rSource in rsSource.result)
                            {
                                var rTarget = new TemperoturyReportRow();
                                rTarget.EmpId = rSource.EmployeeId;
                                rTarget.EmpName = rSource.EmployeeName;
                                rTarget.Date = rSource.AttendDate;
                                rTarget.Type = rSource.ReportType == "1" ? "正常" : "異常";
                                rTarget.AutoKey = Convert.ToInt32(rSource.AutoKey);
                                rTarget.Guid = rSource.Guid;
                                rsTarget.Add(rTarget);
                            }
                            Vdb.Data = rsTarget;
                        }
                    }
                }
            }

            return Vdb;
        }
    }
