CREATE VIEW dbo.JB_HR_Hcode
AS
SELECT     RTRIM(H_CODE_DISP) + '_' + RTRIM(H_NAME) AS sName, RTRIM(H_CODE) AS sHcode, RTRIM(H_NAME) AS sHname, RTRIM(UNIT) AS sUnit, MIN_NUM AS iMin, 
                      IN_HOLI AS bInHoli, RTRIM(YEAR_REST) AS sYearRest, MAX_NUM AS iMax, CHE AS bCheck, RTRIM(ISNULL(DCODE, '')) AS sDcode, RTRIM(ISNULL(SEX, '')) AS sSex, 
                      ABSUNIT AS iAbsUint, DISPLAYFORM AS bDisplayForm, CALOT AS bCalOt, MANG AS bMang, ISNULL(SORT, 0) AS iSort, NOT_DEL AS bNotDel, RTRIM(H_ENAME) 
                      AS sHnameE, ISNULL(FLOW_GO, '0') AS sFlowGo, ISNULL(FLOW_FINAL, '0') AS sFlowFinal, ISNULL(GROUP_CODE, '') AS sGroupCode
FROM         dbo.HCODE

